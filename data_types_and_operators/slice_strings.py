Hw = "Hello world!"
print(Hw)

# Find out how many characters Hw has
print(len(Hw))

# Get the character in the first position in Hw
print(Hw[0])

# Get the last character
print(Hw[-1])

# Get the 2nd last character
print(Hw[-2])

# Prints the string starting from index 2 to the end, "llo world!"
print(Hw[2:])

# Prints the last 3 characters of the string, "ld!"
print(Hw[-3:])

# Prints from the start up to (but not including) index 5, "Hello"
print(Hw[:5])

# Starts from the second, stops at the fifth (doesn't include it)
print(Hw[1:5])