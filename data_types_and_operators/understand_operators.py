# An operator is a symbol that performs an operation (+, -, *, /, %).
# An operand is the value or variable the operator acts on.

x = 5
y = 1

# Addition
add_result = x + y
print("Addition:", add_result)

# Subtraction
subtract_result = x - y
print("Subtraction:", subtract_result)

# Multiplication
multiply_result = x * y
print("Multiplication:", multiply_result)

# Division
divide_result = x / y
print("Division:", divide_result)

# Remainder (modulus)
remainder_result = x % y
print("Remainder:", remainder_result)

# Greater than
print("x > y:", x > y)

# Less than
print("x < y:", x < y)

# Equal to
print("x == y:", x == y)

# Not equal to
print("x != y:", x != y)

# Greater than or equal to
print("x >= y:", x >= y)

# Less than or equal to
print("x <= y:", x <= y)


